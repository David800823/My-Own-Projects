-- Joins Intro
-- Combine two tables and combining them together.

-- Different type of Joins

-- Combine Joins based on two or more tables

-- Self Join, Right Join, Left Join, Inner Join, Cross Join, 
-- Primary Key = Column that is used to uniquely identify each row in a table
-- Foreign Key = is a table that is the primary key in another table.

-- Inner Joins - Return records that have mathcing values in both tables
-- Right Join - Return all records from the right table, and the matched record from the left table
-- Left Join - Return all records from the left table, and the matched records from the right table

-- Full Join (Outer Join)
-- Take all records from all tables

-- Self Join
-- Must do alias
-- Joins table itself

-- Cross Join
-- Returns all possible combination of rows.
-- No ON Statement

-- UNION appends results set vertically
-- Inner Join appends results set horizontally